package javaBrains;

public class JavaVarriables {
    public static void main(String[] args) {
        //Step:-1: Declare and intialize varriable of diffrent data types.
        //int varriable to store an Integer Value
        int age=28;
        System.out.println("age = " + age);
        //long varriable to store a Long Value
        long l= 988555667677755555L;
        System.out.println(l);
        //double varriable to store a double Value
        double d= 93.5644948544;
        System.out.println(d);
        //float varriable to store a floating point Value
        float decimal=3.14f;
        //char varriable to store a Character Value
        char ch='A';
        System.out.println(ch);
        //boolean varriable to store a boolean Value
        boolean isSky= true;
        System.out.println("isSky = " + isSky);
        String name="Aryan";
        System.out.println("name = " + name);
        name="Ayush";
        System.out.println("name = " + name);





    }
}
